#include <iostream>
#include <vector>

using namespace std;

vector<int> build_lps(const string &pat){
    int m = pat.size();
    vector<int> lps(m,0);
    int len = 0;
    int i = 1;
    while(i<m){
        if(pat[i] == pat[len]){
            len++;
            lps[i] = len;
            i++;
        }
        else{
            if(len!=0){
                len = lps[len-1];
            }
            else{
                lps[i] = 0;
                i++;
            }
        }
    }
    return lps;
}

vector<int> kmp_search(const string&text, const string&pattern){
    vector<int> result;
    int n = text.size();
    int m = pattern.size();
    vector<int> lps = build_lps(pattern);
    int i=0; int j = 0;

    while(i < n){
        if(text[i] == pattern[j]){
            i++, j++;
        }
        if(j==m){
            result.push_back(i-j);
            j = lps[j-1];
        }
        else if(i<n && text[i] != pattern[j]){
            if(j!=0){
                j = lps[j-1];
            }
            else{
                i++;
            }
        }
    }
    return result;
}


int main(){
    string text; cin >> text;
    string pattern; cin >> pattern;

    vector<int> res = kmp_search(text, pattern);
    for(int x: res){
        cout << x << " ";
    }
}